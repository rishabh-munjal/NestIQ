// Helper: get price tier color for map heatmap
export function getPriceColor(pricePerSqft: number): string {
  if (pricePerSqft >= 13000) return '#FF4444'; // Premium - Red
  if (pricePerSqft >= 9000) return '#FF8C00';  // Upper Mid - Orange
  if (pricePerSqft >= 6500) return '#FFD700';  // Mid - Yellow
  if (pricePerSqft >= 5000) return '#90EE90';  // Lower Mid - Light Green
  return '#00FF7F';                              // Affordable - Green
}

export function getInvestmentScoreColor(score: number): string {
  if (score >= 90) return '#00FF88';
  if (score >= 80) return '#00D4FF';
  if (score >= 70) return '#FFD700';
  if (score >= 60) return '#FF8C00';
  return '#FF4444';
}
